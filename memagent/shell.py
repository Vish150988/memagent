def detect_shell() -> str:
    """Detect the current shell."""
    shell = os.environ.get("SHELL", "")
    if "zsh" in shell:
        return "zsh"
    if "bash" in shell:
        return "bash"
    if "fish" in shell:
        return "fish"
    if platform.system() == "Windows":
        return "powershell"
    return "bash"


def generate_shell_integration(shell: str | None = None) -> str:
    """Generate shell integration script for the given shell."""
    shell = shell or detect_shell()
    if shell not in SHELL_ALIASES:
        shell = "bash"
    return SHELL_ALIASES[shell]


def install_shell_integration(shell: str | None = None) -> Path:
    """Install shell integration by printing instructions."""
    shell = shell or detect_shell()
    script = generate_shell_integration(shell)
    config_file = _get_shell_config_path(shell)
    return config_file, script


def _get_shell_config_path(shell: str) -> Path:
    """Get the config file path for a shell."""
    home = Path.home()
    paths = {
        "bash": home / ".bashrc",
        "zsh": home / ".zshrc",
        "fish": home / ".config" / "fish" / "config.fish",
        "powershell": home / "Documents" / "PowerShell" / "Microsoft.PowerShell_profile.ps1",
    }
    return paths.get(shell, home / ".bashrc")


def create_wrapper_script(project: str, output: Path | None = None) -> Path:
    """Create a wrapper script that loads memory before opening an agent.

    Usage: ./claude-with-memory -> loads context, then runs claude
    """
    engine = MemoryEngine()
    brief = build_context_brief(engine, project)

    script_content = f"""#!/usr/bin/env bash
# Auto-generated by Memagent — loads context before starting Claude Code

PROJECT="{project}"
echo "Loading Memagent context for $PROJECT..."

# Export context as environment variable for Claude to read
export AGENT_MEMORY_CONTEXT=$(cat <<'EOF'
{brief}
EOF
)

# Start Claude with context pre-loaded
claude "$@"
"""
    if output is None:
        output = Path.cwd() / f"claude-memory-{project}"
    output.write_text(script_content, encoding="utf-8")
    os.chmod(output, os.stat(output).st_mode | 0o111)
    return output
